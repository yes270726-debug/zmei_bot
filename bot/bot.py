class Storage:
    def __init__(self):
        self.users = {}
        self.global_anger = 0
        self.mod_knowledge = {mod: 100 for mod in MODS}  # Змей знает все моды!
        
    def get_user(self, user_id, message):
        if user_id not in self.users:
            name = message.from_user.first_name or "Незнакомец"
            if message.from_user.last_name:
                name += f" {message.from_user.last_name}"
            
            if "катя" in name.lower() or "katya" in name.lower():
                respect = -100
                is_katya = True
            else:
                respect = random.randint(40, 80)
                is_katya = False
            
            self.users[user_id] = {
                'name': name,
                'messages': 0,
                'respect': respect,
                'is_katya': is_katya,
                'last_seen': datetime.now(),
                'provoked': False,
                'favorite_mods': [],
                'mods_discussed': {},
            }
        return self.users[user_id]
    
    def update_anger(self, amount):
        self.global_anger = max(0, min(100, self.global_anger + amount))
    
    def discuss_mod(self, user_id, mod_name):
        if mod_name in MODS:
            if mod_name not in self.users[user_id]['mods_discussed']:
                self.users[user_id]['mods_discussed'][mod_name] = 0
            self.users[user_id]['mods_discussed'][mod_name] += 1

