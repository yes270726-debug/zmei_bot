@dp.message()
async def handle_message(message: types.Message):
    if message.text.startswith('/'):
        return
    
    user_id = message.from_user.id
    user = db.get_user(user_id, message)
    text = message.text.lower().strip()
    
    user['messages'] += 1
    user['last_seen'] = datetime.now()
    
    # Катя - сразу негатив
    if user['is_katya'] or "катя" in text or "katya" in text:
        db.update_anger(5)
        await message.answer(random.choice([
            "Катя? Не люблю!",
            "Не надо про Катю",
            "Катя бесит",
            "Фу, Катя"
        ]))
        return
    
    # Проверка на провокации
    if any(word in text for word in ["плохой змей", "говно", "отстой", "фигня", "создатель плохой"]):
        user['provoked'] = True
        db.update_anger(20)
        await message.answer(random.choice(INSULTS_FINAL))
        return
    
    # Проверяем, упомянул ли пользователь какой-то мод
    mentioned_mod = None
    for mod in MODS:
        mod_lower = mod.lower()
        if mod_lower in text:
            mentioned_mod = mod
            db.discuss_mod(user_id, mod)
            break
    
    # Поиск ответа
    answer = None
    
    # Если упомянули мод - отвечаем про мод
    if mentioned_mod:
        answer = random.choice(MODS_ANSWERS[mentioned_mod])
    
    # Иначе ищем по ключевым словам
    if not answer:
        for key in ALL_ANSWERS:
            if key in text and key != "general":
                # 10% мата если злость высокая
                if db.global_anger > 50 and random.random() < 0.3:
                    answer = random.choice(INSULTS_FINAL)
                else:
                    answer = random.choice(ALL_ANSWERS[key])
                break
    
    # Если ничего не нашли - общие фразы или мат
    if not answer:
        if random.random() < 0.1:  # 10% мата
            answer = random.choice(INSULTS_FINAL)
        else:
            answer = random.choice(ALL_ANSWERS["general"])
    
    # Если не бесили - снижаем злость
    if not user['provoked'] and random.random() < 0.1:
        db.update_anger(-5)
    
    await message.answer(answer)
    
    # Логируем
    print(f"👤 {user['name']}: {message.text}")
    print(f"🐍 Змей: {answer[:50]}...")
    if mentioned_mod:
        print(f"🎮 Обсуждаем мод: {mentioned_mod}")
    print(f"📊 Злость: {db.global_anger}%")
    print("-" * 50)

